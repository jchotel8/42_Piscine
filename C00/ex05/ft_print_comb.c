#include <unistd.h>

void	ft_print_comb(void)
{
	int a[3];

	a[0] = 47;
	while (a[0]++ <= 55)
	{
		a[1] = a[0];
		while (a[1]++ <= 55)
		{
			a[2] = a[1];
			while (a[2]++ <= 56)
			{
				write(1, &a[0], 1);
				write(1, &a[1], 1);
				write(1, &a[2], 1);
				if (a[0] < 55)
					write(1, ", ", 2);
			}
		}
	}
}
