int	ft_is_prime(int nb)
{
	int div;

	div = 2;
	if (nb == 2)
		return (1);
	if (nb <= 1 || !(nb % 2))
		return (0);
	while (div <= (nb / div))
	{
		if (!(nb % div))
			return (0);
		div++;
	}
	return (1);
}
